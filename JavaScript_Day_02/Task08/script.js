let visitors = 100;
console.log(`Pre Increment  : ${++visitors}`);// 100 increased to 101 first, and then printed as 101. now visitors value is 101 
console.log(`Post Increment : ${visitors++}`);// 101 is printed first, afterthat visitors is increased to 102. now visitors is 102
console.log(`Pre Decrement  : ${--visitors}`);// 102 is decreased to 101 first and then printed as 101. now visitors is 101
console.log(`Post Decrement : ${visitors--}`);// 101 is printed first and the decremented to 100. now visitors is 100
